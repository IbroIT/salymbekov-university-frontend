// Функции для получения локализованных данных
export const getLocalizedText = (obj, field, language) => {
  if (!obj) return '';
  
  switch (language) {
    case 'kg':
      return obj[`${field}_kg`] || obj[field];
    case 'en':
      return obj[`${field}_en`] || obj[field];
    default:
      return obj[field];
  }
};

